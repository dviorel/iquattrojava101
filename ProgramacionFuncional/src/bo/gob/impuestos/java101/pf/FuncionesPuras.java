package bo.gob.impuestos.java101.pf;

public class FuncionesPuras {

    static int sumar(int a, int b){
        return a + b;
    }

    public static void main(String[] args) {
        System.out.println(sumar(3, 4));
        System.out.println(sumar(3, 4));
        System.out.println(sumar(3, 4));
        System.out.println(sumar(3, 4));
        System.out.println(sumar(3, 4));
        System.out.println(sumar(3, 4));
        System.out.println(sumar(3, 4));
    }

}
